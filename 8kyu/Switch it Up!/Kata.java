public class Kata
{
  public static String switchItUp(int number)
  {
    String switchItUp="";
    switch (number){
     case 0 : 
     switchItUp="Zero";
     break;   
     case 1:
     switchItUp="One";
     break;
     case 2:
     switchItUp="Two"; 
     break;
     case 3:
     switchItUp="Three";  
     break;
     case 4:
     switchItUp="Four"; 
     break;
     case 5 : 
     switchItUp="Five";
     break;
     case 6 : 
     switchItUp="Six";
     break; 
      case 7 : 
     switchItUp="Seven";
     break;
     case 8 : 
     switchItUp="Eight";
     break;
     case 9 : 
     switchItUp="Nine";
     break;   
  }
    return switchItUp;
  }
}